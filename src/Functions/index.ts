//Functions in typescript
//in typescript all the functions are typed and the parameters passing to the functions are typed
function add(a:number,b:number):number{
    return a + b;
}

console.log(add(3,4));


//arrow funcions in typescript
const multiply = (a:number,b:number):number => {
    return a * b
}

console.log(multiply(4,3))

